<h1>Thinking of <br/>Bue</h1>

<article class='body article'>
<p>Thongbue Wongbandue, also known as Tom, Tommy, Bue, Tito Bue, or, to his family, simply as Dad, passed away on Friday, March 28, 2025, at the age of 76. Born in Thailand, Dad came to the US at the age of 27 to pursue a degree in engineering and worked in NYC kitchens to pay his way. Engineering never stuck, but he built a 40+ year career in the restaurant industry, doing everything from washing dishes to making pastries to working the line in a world-class French brigade. A cook by trade, but a proud host at heart, there was no greater joy than having his house full of our friends (and their parents, I guess) and making sure everyone was well-fed and having fun. Whether it was his beef wellington, perfectly grilled steaks, or generously stuffed lobster tails, everyone left our home smiling and very sleepy. We didn't even have to clean up after him, because he did that too.</p>

<p>To be in his presence was to watch a man who never stopped moving. When he wasn't cooking (at home or at work), he was tackling new projects around the house, both of his own creation and from our family's neverending wishlist. A self-taught woodworker, he built desks, headboards, bookshelves, and more; plus an ornate, toddler-sized dollhouse, which no visitor to the house will ever forget. He kept the yard pristine, trimmed the trees and hedges, and even planted a garden. One day, we suddenly had a koi pond. That was Dad: impulsive, dedicated, and always in need of a project.</p>

<p>In addition to being the host with the most and man with a thousand tools, he was also stubborn, independently minded, and fearless (except for when it came to ghosts). Most of all, he was incredibly proud. He relished his perpetual good looks and sense of style. He loved the home he helped to build. And despite his lack of medals, trophies, or even a gym membership, he always bragged about being a stellar athlete. But above all, he was proud of us, his children. As one half of a literal around-the-clock parenting tag-team--he and Mom keeping opposite hours to keep us fed, transported, and cared for--Dad would have dinner ready on the table for Mom when she woke up for her night shift, along with a thoughtfully packed meal that would be sure to make all of her coworkers jealous. Not only did he make sure we had everything we needed, but he loved to spoil us with special treats like chocolate-covered strawberries, warm cookies, and a deep freezer full of tartufo, all smuggled from his work kitchen. He'd even bring extras for our friends.</p>

<p>Whether driving Jamie to gymnastics meets in Pennsylvania or driving me to dance competitions also in Pennsylvania, he'd do anything, like drive to Pennsylvania, to support us and our interests. And if he saw Jamie flipping and tumbling on the spring floor, it was only a matter of time before heâ€™d try flipping on the trampoline and (after not quite landing it) tumbling across the lawn in our backyard. As we got older and our interests shifted, so did his support for us. He loved us more than anything in the world, except maybe Muffin, our cockapoo, who we lost in 2021 and is fully deserving of her own memorial page (stay tuned).</p>

<p>His death leaves us missing his laugh, his playful sense of humor, and oh so many good meals. Fortunately, he's left behind his familial dedication with Mom, his snobby taste in food with me, and an incredible, like just perfect, head of hair with Jamie. And for our family as a whole, he's imparted one massive dollhouse we don't know what to do with, but you'll never be able to take from us in a million years. Love you, Dad.</p>

<p class="signature">Julie Wongbandue</p>

